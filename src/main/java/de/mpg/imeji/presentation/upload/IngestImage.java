package de.mpg.imeji.presentation.upload;

import java.io.File;

public class IngestImage {
	private File file;
	private String name;
	
	public IngestImage(){
		
	}
	public File getFile() {
		return file;
	}
	public void setFile(File file) {
		this.file = file;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
